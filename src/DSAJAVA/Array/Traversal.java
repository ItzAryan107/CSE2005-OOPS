package DSAJAVA.Array;
// traversal means to visit all the element of an Array
//every index of array automatically get initialized with zero
public class Traversal {

    public static void main(String[] args) {
        int[]a={1,2,3,4,5,6,7,8};

        for (int b:a){
            System.out.println(b);
        }
    }
}
